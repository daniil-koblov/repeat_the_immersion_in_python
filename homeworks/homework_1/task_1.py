width = int(input('Введите ширину рамки: '))
heigth = int(input('Введите высоту рамки: '))

for i in range(heigth):
    if i == 0:
        for k in range(width):
            if k == 0:
                print('|')
            elif k > 0 and k < width - 1:
                print('-')
            else:
                print('|')
    elif i > 0 and i < heigth - 1:
        for k in range(width):
            if k == 0:
                print('|')
            elif k > 0 and k < width - 1:
                print(' ')
            else:
                print('|')
    else:
        for k in range(width):
            if k == 0:
                print('|')
            elif k > 0 and k < width - 1:
                print('-')
            else:
                print('|')
